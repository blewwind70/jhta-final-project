package com.es.pos.vo;

public class PosLoginForm {

	private int posId;
	private long empId;
	private long empPwd;
	
	public int getPosId() {
		return posId;
	}
	public void setPosId(int posId) {
		this.posId = posId;
	}
	public long getEmpId() {
		return empId;
	}
	public void setEmpId(long empId) {
		this.empId = empId;
	}
	public long getEmpPwd() {
		return empPwd;
	}
	public void setEmpPwd(long empPwd) {
		this.empPwd = empPwd;
	}

}
